package cf.rittzyradio.ritzzyradio;

public class schlpumodel {
    private String name;
    private String timing;

    public schlpumodel(String name, String timing) {
        this.name = name;
        this.timing = timing;
    }

    public schlpumodel(){

    }

    public String getName() {
        return name;
    }

    public String getTiming() {
        return timing;
    }
}
