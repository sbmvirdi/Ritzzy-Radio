package cf.rittzyradio.ritzzyradio;

public class schsitamodel {

    private String name;
    private String timing;

    public schsitamodel(String name, String timing) {
        this.name = name;
        this.timing = timing;
    }

    public schsitamodel() {
    }

    public String getName() {
        return name;
    }

    public String getTiming() {
        return timing;
    }
}
